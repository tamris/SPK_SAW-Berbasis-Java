     private void loadAlternatif() {
        try {
            st = c.createStatement();
            String sql = "SELECT id, nama FROM alternatif";
            rs = st.executeQuery(sql);
            while (rs.next()) {
                String id = rs.getString("id");
                String nama = rs.getString("nama");
                cmbAlt.addItem(nama); // Tambahkan nama ke JComboBox
                alternatifMap.put(nama, id); // Simpan pasangan nama dan id ke dalam Map
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat memuat data alternatif: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
     
    private void TampilDataPenilaian(){
        try {
            st = c.createStatement();
            String sql = "SELECT k.id, a.nama, k.ipk, k.tes_pemrograman, k.kemampuan_mengajar, k.nilai_referensi, k.kerjasama "
                    + "FROM kriteria k "
                    + "JOIN alternatif a ON k.id_alternatif = a.id";
            rs = st.executeQuery(sql);

            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("No");
            model.addColumn("Nama");
            model.addColumn("IPK");
            model.addColumn("Tes Pemrograman");
            model.addColumn("Kemampuan Mengajar");
            model.addColumn("Nilai Referensi");
            model.addColumn("Kerjasama");

            int no = 1;

            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);

            while (rs.next()) {
                Object[] data = {
                    no++,
                    rs.getString("nama"),
                    rs.getDouble("ipk"),
                    rs.getDouble("tes_pemrograman"),
                    rs.getDouble("kemampuan_mengajar"),
                    rs.getDouble("nilai_referensi"),
                    rs.getDouble("kerjasama")
                };
                model.addRow(data);
            }
            tblPenilaian.setModel(model);
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat memuat data penilaian: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

