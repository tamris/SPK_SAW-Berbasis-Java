    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
          try {
        st = c.createStatement();
        if (cmbAlt.getSelectedItem() == null ||
            txtIpk.getText().equals("") ||
            txtTestPemrograman.getText().equals("") ||
            txtKemampuanMengajar.getText().equals("") ||
            txtNilaiReferensi.getText().equals("") ||
            txtKerjasama.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Data Tidak Boleh Kosong", "Validasi Data", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        String selectedNama = cmbAlt.getSelectedItem().toString();
        String idAlternatif = alternatifMap.get(selectedNama);

        if (btnSimpan.getText().equals("Simpan")) {
            // Cek apakah id_alternatif sudah ada di tabel kriteria
            String cek = "SELECT * FROM kriteria WHERE id_alternatif = '" + idAlternatif + "'";
            rs = st.executeQuery(cek);
            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "Penilaian untuk alternatif ini sudah ada. Tidak bisa menambahkan kembali.");
            } else {
                String sql = "INSERT INTO kriteria (id_alternatif, ipk, tes_pemrograman, kemampuan_mengajar, nilai_referensi, kerjasama) VALUES ('"
                        + idAlternatif + "', '"
                        + Double.parseDouble(txtIpk.getText()) + "', '"
                        + Double.parseDouble(txtTestPemrograman.getText()) + "', '"
                        + Double.parseDouble(txtKemampuanMengajar.getText()) + "', '"
                        + Double.parseDouble(txtNilaiReferensi.getText()) + "', '"
                        + Double.parseDouble(txtKerjasama.getText()) + "')";
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Data Berhasil disimpan");
                bersihPenilaian();
                TampilDataPenilaian();
            }
        } else {
            String update = "UPDATE kriteria SET ipk = '" + Double.parseDouble(txtIpk.getText())
                    + "', tes_pemrograman = '" + Double.parseDouble(txtTestPemrograman.getText())
                    + "', kemampuan_mengajar = '" + Double.parseDouble(txtKemampuanMengajar.getText())
                    + "', nilai_referensi = '" + Double.parseDouble(txtNilaiReferensi.getText())
                    + "', kerjasama = '" + Double.parseDouble(txtKerjasama.getText())
                    + "' WHERE id_alternatif = '" + idAlternatif + "'";
            st.executeUpdate(update);
            JOptionPane.showMessageDialog(null, "Data berhasil diubah");
            bersihPenilaian();
            TampilDataPenilaian();
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

